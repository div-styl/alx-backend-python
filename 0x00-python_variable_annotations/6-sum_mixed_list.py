#!/usr/bin/env python3
"""
sum_mixed_list which takes a list mxd_lst
of integers and floats and returns their sum as a float.
"""


def sum_mixed_list(mxd_lst: list) -> float:
    """sum_mixed_list which takes a list mxd_lst
    of integers and floats and returns their sum as a float.
    """
    return sum(mxd_lst)
